/**
 * Calculates the sum of two numbers by adding them together.
 * @param augend First number in the expression
 * @param addend Number added to the augend
 * @returns The sum of the two numbers
 */
export function add(augend: number, addend: number) {
  return augend + addend;
}

/**
 * Calculates the difference of two numbers by subtracting the subtrahend from the minuend.
 * @param minuend First number in the expression
 * @param subtrahend Value to subtract from the minuend
 * @returns Returns the difference of the values.
 */
export function sub(minuend: number, subtrahend: number) {
  return minuend - subtrahend;
}
